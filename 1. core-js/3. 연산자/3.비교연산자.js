var a = 5;
var b = '5';

// a랑 b가 같냐고 물어보고 싶으면 ==
console.log(a === b);
// a랑 b가 다르냐고 물어보고 싶으면 !=
console.log(a !== b);

// 텍스트 5랑 숫자 5가 같다고??
