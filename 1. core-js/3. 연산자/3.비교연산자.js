// 문자열 대소 비교 (이게 가능해?)
console.log('a' === 'A'); // 대소문자 구분을 하기 때문에 false가 나와야함이 맞음.
console.log('안녕하세요' === '안녕 하세요'); // 공백도 글자수로 치기 때문에 다르다.

console.log('a' < 'A'); // 문자를 숫자로 변환해서 비교하네
// 이러면 a가 더 크다고 나옴. why?
// 모든 문자에는 숫자가 부여되어 있다.
// A: 65, a: 97, 가: 44032 <== 이 정도는 기억해둘 필요가 있다.


console.log('가' < '바'); // '바'가 더 크다. 사전에서 앞에서 나오면 작은거고, 뒤에서 나오면 큰거다.


console.log('ace' < 'ade'); // 글자수가 같을 경우 비교하기 시작함.
// 애초에 글자수가 다르다면 연산 자체를 하지 않음.
// 한글자씩 비교하다가 같지 않고 한 쪽이 더 크거나 작은 구도가 나오면 거기서 연산을 끝냄.

